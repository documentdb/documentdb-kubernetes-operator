package otel

import (
	"fmt"
	"strings"

	dbpreview "github.com/documentdb/documentdb-operator/api/preview"
)

// ConfigMapName returns the OTel ConfigMap name for a given DocumentDB cluster.
func ConfigMapName(clusterName string) string {
	return fmt.Sprintf("%s-otel-config", clusterName)
}

// GenerateBaseYAML generates the OTel Collector base.yaml content from a MonitoringSpec.
// This is a minimal config with an OTLP receiver, resource processor, and exporters.
// Additional receivers (engine metrics, host metrics) will be added in follow-up PRs.
func GenerateBaseYAML(clusterName, namespace string, spec *dbpreview.MonitoringSpec) string {
	var b strings.Builder

	b.WriteString("# Auto-generated by documentdb-operator. Do not edit.\n")
	b.WriteString("receivers:\n")
	b.WriteString("  otlp:\n")
	b.WriteString("    protocols:\n")
	b.WriteString("      grpc:\n")
	b.WriteString("        endpoint: \"0.0.0.0:4317\"\n")
	b.WriteString("\n")

	b.WriteString("processors:\n")
	b.WriteString("  batch:\n")
	b.WriteString("    send_batch_size: 1024\n")
	b.WriteString("    timeout: 5s\n")
	b.WriteString("\n")
	b.WriteString("  resource:\n")
	b.WriteString("    attributes:\n")
	b.WriteString(fmt.Sprintf("      - key: documentdb.cluster\n        value: %q\n        action: upsert\n", clusterName))
	b.WriteString(fmt.Sprintf("      - key: k8s.namespace.name\n        value: %q\n        action: upsert\n", namespace))
	b.WriteString("      - key: k8s.pod.name\n        value: ${POD_NAME}\n        action: upsert\n")
	b.WriteString("\n")

	// Exporters
	b.WriteString("exporters:\n")
	b.WriteString("  debug:\n")
	b.WriteString("    verbosity: basic\n")

	exporterNames := []string{"debug"}

	if spec.Exporter != nil && spec.Exporter.OTLP != nil && spec.Exporter.OTLP.Endpoint != "" {
		b.WriteString("  otlp:\n")
		b.WriteString(fmt.Sprintf("    endpoint: %q\n", spec.Exporter.OTLP.Endpoint))
		b.WriteString("    tls:\n")
		b.WriteString("      insecure: true\n")
		exporterNames = append(exporterNames, "otlp")
	}

	b.WriteString("\n")

	// Service pipeline
	b.WriteString("service:\n")
	b.WriteString("  pipelines:\n")
	b.WriteString("    metrics:\n")
	b.WriteString("      receivers:\n")
	b.WriteString("        - otlp\n")
	b.WriteString("      processors:\n")
	b.WriteString("        - resource\n")
	b.WriteString("        - batch\n")
	b.WriteString("      exporters:\n")
	for _, name := range exporterNames {
		b.WriteString(fmt.Sprintf("        - %s\n", name))
	}

	return b.String()
}
